               <article>
                  <p class="intro"><strong>Modernizr</strong> is an open-source JavaScript library that helps you build the next generation of HTML5 and CSS3-powered websites.</p>

                  <h2>Why use Modernizr?</h2>
                  <p>Taking advantage of the new capabilities of HTML5 and CSS3 can mean sacrificing control over the experience in older browsers. Modernizr 2 is your starting point for making the best websites and applications that work exactly right no matter what browser or device your visitors use.</p>

                  <p>Thanks to the new Media Query tests and built-in <a href="http://yepnopejs.com/" rel="external">YepNope.js</a> micro-library as <code>Modernizr.load()</code>, you can now combine feature detection with media queries and conditional resource loading. That gives you the power and flexibility to optimize for every circumstance.</p>

                  <p>Check out the <a href="/docs/#features-css">full list of features</a> that Modernizr detects, or learn more about <a href="/docs/#load">conditional resource loading with Modernizr</a>.</p>
               </article>
